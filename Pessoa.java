package Pessoa;

public class Pessoa {
	String nome;
	String endereco;
	int idade;
	
	public void correr() {
		System.out.println("A pessoa está correndo");
	}

	public void dancar() {
		System.out.println("A pessoa está dançando");
	}
	
	public Pessoa(String nome, String endereco, int idade) {
		this.nome=nome;
		this.endereco=endereco;
		this.idade=idade;
		
	
	}
 		

}
 	
		
 	